func romanToInt(s string) int {
	if s == "" {
		return 0
	}
	var (
		values = map[string]int{
			"I":  1,
			"V":  5,
			"X":  10,
			"L":  50,
			"C":  100,
			"D":  500,
			"M":  1000,
			"IV": 4,
			"IX": 9,
			"XL": 40,
			"IL": 49,
			"IC": 99,
			"XC": 90,
			"ID": 499,
			"XD": 490,
			"CD": 400,
			"IM": 999,
			"XM": 990,
			"CM": 900,
		}
		pos   = 0
		total = 0
	)
	for pos < len(s) {
		if pos+1 < len(s) {
			if v, ok := values[s[pos:pos+2]]; ok {
				total += v
				pos += 2
				continue
			}
		}
		total += values[string(s[pos])]
		pos++
	}
	return total
}

